// While loop
let i = 0;
while (i < 3) { // shows 0, then 1, then 2
    console.log(i);
    i++;
}

// Do loop  do loop mean initialy loop start first time.
let x = 0;
do {
    console.log(x);
    x++;
} while (x < 3);

// For loop
for (let i = 0; i < 3; i++) { // shows 0, then 1, then 2
    console.log(i);
}