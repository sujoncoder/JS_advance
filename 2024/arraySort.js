// sort
const fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log("Now sort array is: ", fruits.sort());
console.log(fruits);

// toSorted
const fToSort = ["Banana", "Orange", "Apple", "Mango"];
console.log("toSorted is: ", fToSort.toSorted());
console.log(fToSort);

// reverse
const fruits1 = ["Banana", "Orange", "Apple", "Mango"];
console.log("Reverse array is: ", fruits1.reverse());

// toReversed
const fruitstoReversed = ["Banana", "Orange", "Apple", "Mango"];
console.log("toReversed is: ", fruitstoReversed.toReversed());
console.log(fruitstoReversed);
